        <p style="text-align:justify;">
			Wounds experienced during and exploration space flight may range from minor abrasions to major lacerations that will require control of bleeding, 
            debridement, and wound closure prior to bandaging. It is necessary to contain and control the wound site so that it does not get contaminated by
            debris floating through the spacecraft and so that the blood and tissue does not contaminate the spacecraft. To address this wound care need, the 
            team of investigators from the University of Louisville and Carnegie Mellon University are developing a containment chamber (dome) that can be 
            placed over the wound site and sealed to the adjacent skin. The dome has inflow and outflow ports for infusion of an immersion fluid (saline) and 
            instrument ports for the passage of medical instruments to allow wound debridement, suction, and closure. 
            </p>
            
            <p>
            The immersion fluid system could potentially be used with a wide variety of ground-based surgical procedures including brain and 
            spinal cord surgery to facilitate the management of intraoperative bleeding and arthroscopic surgery to improve control of joint capsule 
            distension while minimizing the use of distension fluid. The principle of operation is based on the application of appropriate hydrostatic 
            pressure to the immersion fluid surrounding a bleeding vessel to staunch bleeding while still allowing perfusion through the vessel. 
            </p>
            
            <p>
            Research efforts include staunching of bleeding by temporary elevation of immersion fluid pressure, regulation of immersion fluid pressure, 
            purging to maintain the clarity of the surgical field of view, evaluation of the dome/skin interface, development of leak-free trocars for 
            endoscopic instruments.  Evaluation experiments conducted in 0-G on the NASA parabolic flight aircraft have demonstrated good proof-of-concept 
            performance of the surgical containment system.
