				<p>
				Many locomotive devices have several internal degrees of freedom, which pose great challenges to planning and control. 
                Our goal is to use a dimensionality reduction technique to simplify the representation of dynamic systems, 
                and plan motions for these systems in an intuitive fashion. Observations of biological systems show that coupled
                body waves can be used to manage locomotor complexity and achieve higher performance motions. These coupled body waves 
                can be mathematically represented as shape bases. We devise planning algorithms to search for appropriate shape bases, 
                and design novel gaits using the derived shape bases. These algorithms allow us to better understand biological locomotion, 
                as well as design capable motions for robotic systems.

