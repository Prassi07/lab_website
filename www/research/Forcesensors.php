			<h3><b> Robotic Force Perception</h3> 
			<p>
				 The sense of touch plays an essential role in our daily life, such as grasping,   typing, social interaction and even detecting dangerous conditions. However, modern robots are mostly lacking accurate and multifunctional touch sensors like the tactile receptors perceptors in our skin. Giving robot the capability of touch and sense, hence enable it to perceive and interact with the environment, is one of the research focus in the CMU biorobotics lab.

				In the last few years, we had been working on a various version of custom design force sensors for our locomotion snake and legged system. Besides, we also integrated a miniature version of the force sensor on our medical robotics systems and developed software system to process the tactile information for the robot or human operators. 

				Our ultimate goal is to create force sensors that are low-cost, highly accurate, yet easy to integrate. By enabling the design freedom of using force sensors on any robotic system, such as small end-effector or high-load impact joints, we believe this type of force sensing research will eventually enable robotic researchers and engineers to explain more sophiscated robotic algorithms and produce more significant impacts on real-world robotic applications. 

