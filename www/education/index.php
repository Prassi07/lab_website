		<h4>Philosophy</h4>
		<p>
		The philosophy of using construction and implementation to reinforce theory permeates
		Professor Choset's courses. In Professor Choset's undergraduate robotics course,
		students use LEGO robotics labs, developed by Professor Choset and his students,
		to reinforce the rigorous and theoretical materials presented in class. Every two
		weeks, lecture material covers the underlying mathematics and algorithms and via
		construction of a programmable three-dimensional artifact, the lab experiences
		seriously motivate students to synthesize lessons, critically explore beyond them,
		and then think creatively with meta-lessons. Professor Choset termed this style of 
		education as directed constructionism because it strikes a balance between 
		conventional on-way lectures and modern constructionist approaches.
		</p>
		<p>
		However, Professor Choset's long-term education goal is to bring this directed
		constructionist philosophy to the primary school levels. Robotics is a powerful
		vehicle to inspire and teach. Based on volunteer work with robotics contests FIRST
		and Botball, Professor Choset and his students have seen that robotics is an
		educational tool that dramatically elevates children's, interest in sciences and
		mathematics. A robot is a hook that induces curiosity and excitement. Once they see
		a robot in motion, both boys and girls, want to build their own robot. To do so,
		they must understand mathematics, science, and technology. The benefits of robotics
		in education, however, go well beyond the technical skills. These project-based
		experiences allow students to develop team building and inter-personal skills that
		jobs of the 21st Century will demand. 
