			<h3><b>Toroidal Skin Drive</b> for Snake Robot Locomotion</h3>
			<p>
				Small robots have the potential to access confined spaces where 
				humans cannot go. However, the mobility of wheeled and tracked systems
				is severely limited in cluttered environments. Snake robots using
				biologically inspired gaits for locomotion can provide better access
				in many situations, but are slow and can easily snag. 
			</p>
			<p>
