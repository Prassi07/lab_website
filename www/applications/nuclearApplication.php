			<p><b>Remote access to radioactive areas</b><br>
			The robot's wide range of locomotion capabilities allow it to remotely access radioactive environments, while keeping workers away from harmful radiation.</p>
			
			
			<p><b>Reach locations that other equipment cannot reach</b><br>
			Nuclear power plants, vitrification plants, or storage locations such as tasks contain equipment, tanks, and piping networks that conventional robots have trouble reaching or inspecting.  The snake robot's unique capabilities enable it to access these otherwise unreachable locations.</p>
			
			
			<p><b>Access through small ports, vents, or openings</b><br>
			The robots can squeeze through small openings to access tanks or storage casks.  This enables remote visual inspection (RVI) or nondestructive testing (NDE) without the need to cut into the equipment or open the radioactive cask.</p>
