			<h3>Nuclear Inspection <b>Applications</b></h3>
				
			<p>Certain areas of nuclear power plants or nuclear waste 
			storage locations are highly radioactive and cannot be safely 
			accessed by people. While a variety of conventional robots 
			have been developed to help provide access to radioactive 
			locations, there are still limitations to where these robots 
