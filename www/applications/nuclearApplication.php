			locations within these radioactive environments. In 
			particular, these robots could address the challenge of 
			inspecting inside of dry storage casks, waste storage tanks, 
			piping within nuclear power plants or waste vitrification 
			plants, or in other radioactive locations.  The robot's ability to swim could also enable them to perform underwater tasks such as spent fuel pool inspection or reaching locations within boiling water reactors.</p>
